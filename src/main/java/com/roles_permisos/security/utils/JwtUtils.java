package com.roles_permisos.security.utils;

import java.util.Date;
import java.util.Map;
import java.util.UUID;
import java.util.stream.Collectors;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.security.core.Authentication;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.stereotype.Component;

import com.auth0.jwt.JWT;
import com.auth0.jwt.JWTVerifier;
import com.auth0.jwt.algorithms.Algorithm;
import com.auth0.jwt.exceptions.JWTVerificationException;
import com.auth0.jwt.interfaces.Claim;
import com.auth0.jwt.interfaces.DecodedJWT;

@Component  
public class JwtUtils {

    @Value("${security.jwt.private.key}")
    private String privateKey;

    @Value("${security.jwt.user.generator}")
    private String userGenerator;

    // Creacion de token
    public String createToken(Authentication authentication){

        Algorithm algorithm = Algorithm.HMAC256(privateKey);

        // queda en el context holder
        String username = authentication.getPrincipal().toString();

        String authorities = authentication.getAuthorities()
            .stream()
            .map(GrantedAuthority::getAuthority)
            .collect(Collectors.joining(","));

        String jwtToken = JWT.create()
            .withIssuer(this.userGenerator)
            .withSubject(username)
            .withClaim("authorities", authorities)
            .withIssuedAt(new Date())
            .withExpiresAt(new Date(System.currentTimeMillis()+(30*60000)))
            .withJWTId(UUID.randomUUID().toString())
            .sign(algorithm);
        return jwtToken;

    }


    // decodificar y validar nuestros tokens
    public DecodedJWT validateToken(String token){
        try{
            Algorithm algorithm = Algorithm.HMAC256(privateKey);
            JWTVerifier verifier = JWT.require(algorithm)
            .withIssuer(this.userGenerator)
            .build();

            // si todo esta ok no genera ninguna exepcion y nos devuelve el jwt decodificado
            DecodedJWT decodedJWT = verifier.verify(token);
            return decodedJWT;
        }
        catch(JWTVerificationException exception){
            throw new JWTVerificationException("Token invalido no esta autorizado");
        }
    }


    // metodo para obtener el usuario/username
    public String extracUsername(DecodedJWT decodedJWT){
        return decodedJWT.getSubject().toString();
    }
    // metodo para obtener un claim en particular
    public Claim getSpecificClaim(DecodedJWT decodedJWT,String claimName){
        return decodedJWT.getClaim(claimName);
    }

    // metodo para obtener todos los claims

    public Map<String,Claim> returnAllClains(DecodedJWT decodedJWT){
        return decodedJWT.getClaims();
    }



}
